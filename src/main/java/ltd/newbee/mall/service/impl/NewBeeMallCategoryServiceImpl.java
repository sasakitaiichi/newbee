    @Override
    public PageResult selectCategoryId(PageQueryUtil pageUtil) {//mapper.selectByCategoryId(19)
        final String PRICE = "price";
        final String NEW = "new";
        final String DEFAULT = "default";
        String ORDER_BY = (String) pageUtil.get("orderBy");

        List<Long> goodsList = new ArrayList<Long>();
        List<NewBeeMallGoods> goodsList2 = new ArrayList<NewBeeMallGoods>();
        goodsList = goodsCategoryMapper.selectCategoryId(pageUtil);
        int total = goodsMapper.getTotalNewBeeMallGoodsBySearch(pageUtil);//翻页
        //int total = 15;
        List<NewBeeMallSearchGoodsVO> newBeeMallSearchGoodsVOS = new ArrayList<>();
        if (!CollectionUtils.isEmpty(goodsList)) {
            for (int i = 0; i < goodsList.size(); i++) {
                List<NewBeeMallGoods> tempGoodsList = new ArrayList<NewBeeMallGoods>();//一時List
                List<NewBeeMallGoods> sortedGoodsList = new ArrayList<NewBeeMallGoods>();//ソート済みのlist
                tempGoodsList = goodsMapper.findNewBeeMallGoodsListByGoodsCategoryId(goodsList.get(i));//categoryId

                if (PRICE.equals(ORDER_BY)) {//
                    sortedGoodsList = tempGoodsList.stream().sorted(Comparator.comparing(NewBeeMallGoods::getSellingPrice).reversed()).collect(Collectors.toList());
                }
                if (NEW.equals(ORDER_BY)) {// && (PARENT_ID == PARENT_IDW)
                    sortedGoodsList = tempGoodsList.stream().sorted(Comparator.comparing(NewBeeMallGoods::getGoodsId).reversed()).collect(Collectors.toList());//

                }
                if (DEFAULT.equals(ORDER_BY)) {
                    sortedGoodsList = tempGoodsList.stream().sorted(Comparator.comparing(NewBeeMallGoods::getStockNum).reversed()).collect(Collectors.toList());//.reversed()
                }
                if (sortedGoodsList.size() > 4) {
                    sortedGoodsList = sortedGoodsList.subList(0, 5);

                }

                for (int j = 0; j < sortedGoodsList.size(); j++) {
                    goodsList2.add(sortedGoodsList.get(j));//sortedGoodsListをgoodList2に追加して
                }

            }
            newBeeMallSearchGoodsVOS = BeanUtil.copyList(goodsList2, NewBeeMallSearchGoodsVO.class);
            for (NewBeeMallSearchGoodsVO newBeeMallSearchGoodsVO : newBeeMallSearchGoodsVOS) {
                String goodsName = newBeeMallSearchGoodsVO.getGoodsName();
                String goodsIntro = newBeeMallSearchGoodsVO.getGoodsIntro();
                // 字符串过长导致文字超出的问题
                if (goodsName.length() > 28) {
                    goodsName = goodsName.substring(0, 28) + "...";
                    newBeeMallSearchGoodsVO.setGoodsName(goodsName);
                }
                if (goodsIntro.length() > 30) {
                    goodsIntro = goodsIntro.substring(0, 30) + "...";
                    newBeeMallSearchGoodsVO.setGoodsIntro(goodsIntro);
                }
            }


        }
        PageResult pageResult = new PageResult(newBeeMallSearchGoodsVOS, total, pageUtil.getLimit(), pageUtil.getPage());

        return pageResult;
    }

